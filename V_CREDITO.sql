  AND C.CVE_EMPRESA (+)        = P.CVE_EMPRESA
  AND C.ID_PRESTAMO (+)        = P.ID_PRESTAMO
  AND C.ID_FORMA_APLICACION (+)= 1
  AND C.ID_CARGO_COMISION (+)  = 9
  AND O.CVE_GPO_EMPRESA        = P.CVE_GPO_EMPRESA
  AND O.CVE_EMPRESA            = P.CVE_EMPRESA
  AND O.Id_Producto            = P.Id_Producto
  AND Me.Cve_Gpo_Empresa       = P.Cve_Gpo_Empresa
  AND Me.Cve_Empresa           = P.Cve_Empresa
  AND ME.CVE_METODO            = P.CVE_METODO
  AND S.CVE_GPO_EMPRESA        = P.CVE_GPO_EMPRESA
  AND S.CVE_EMPRESA            = P.CVE_EMPRESA
  AND S.ID_SUCURSAL            = P.ID_SUCURSAL
  AND D.CVE_GPO_EMPRESA (+)    = S.CVE_GPO_EMPRESA
  AND D.CVE_EMPRESA (+)        = S.CVE_EMPRESA
  AND D.ID_DOMICILIO (+)       = S.ID_DIRECCION
  AND PE.CVE_GPO_EMPRESA       = P.CVE_GPO_EMPRESA
  AND PE.CVE_EMPRESA           = P.CVE_EMPRESA
  AND PE.ID_PERIODICIDAD       = P.ID_PERIODICIDAD_PRODUCTO
  AND PET.CVE_GPO_EMPRESA      = P.CVE_GPO_EMPRESA
  AND PET.CVE_EMPRESA          = P.CVE_EMPRESA
  AND PET.ID_PERIODICIDAD      = P.ID_PERIODICIDAD_TASA
  AND T.CVE_GPO_EMPRESA (+)    = P.CVE_GPO_EMPRESA
  AND T.CVE_EMPRESA (+)        = P.CVE_EMPRESA
  AND T.ID_PRESTAMO (+)        = P.ID_PRESTAMO
  AND UAF.CVE_GPO_EMPRESA (+)  = P.CVE_GPO_EMPRESA
  AND UAF.CVE_EMPRESA (+)      = P.CVE_EMPRESA
  AND UAF.CVE_USUARIO (+)      = P.CVE_ASESOR_FUNDADOR
  AND PAF.CVE_GPO_EMPRESA (+)  = UAF.CVE_GPO_EMPRESA
  AND PAF.CVE_EMPRESA (+)      = UAF.CVE_EMPRESA
  AND PAF.ID_PERSONA (+)       = UAF.ID_PERSONA
  AND UAA.CVE_GPO_EMPRESA (+)  = P.CVE_GPO_EMPRESA
  AND UAA.CVE_EMPRESA (+)      = P.CVE_EMPRESA
  AND UAA.CVE_USUARIO (+)      = P.CVE_ASESOR_FUNDADOR
  AND PAA.CVE_GPO_EMPRESA (+)  = UAA.CVE_GPO_EMPRESA
  AND PAA.CVE_EMPRESA (+)      = UAA.CVE_EMPRESA
  AND PAA.ID_PERSONA (+)       = UAA.ID_PERSONA
  AND R.CVE_GPO_EMPRESA (+)    = S.CVE_GPO_EMPRESA
  AND R.CVE_EMPRESA (+)        = S.CVE_EMPRESA
  AND R.ID_REGIONAL (+)        = S.ID_REGIONAL
  AND UCS.CVE_GPO_EMPRESA (+)  = S.CVE_GPO_EMPRESA
  AND UCS.CVE_EMPRESA (+)      = S.CVE_EMPRESA
  AND UCS.CVE_USUARIO (+)      = S.CVE_USUARIO_COORDINADOR
  AND PCS.CVE_GPO_EMPRESA (+)  = UCS.CVE_GPO_EMPRESA
  AND PCS.CVE_EMPRESA (+)      = UCS.CVE_EMPRESA
  AND PCS.ID_PERSONA (+)       = UCS.ID_PERSONA
  AND L.CVE_GPO_EMPRESA (+)    = P.CVE_GPO_EMPRESA
  AND L.CVE_EMPRESA (+)        = P.CVE_EMPRESA
  AND L.NUM_LINEA (+)          = P.NUM_LINEA
  AND F.CVE_GPO_EMPRESA (+)    = L.CVE_GPO_EMPRESA
  AND F.CVE_EMPRESA (+)        = L.CVE_EMPRESA
  AND F.CVE_FONDEADOR (+)      = L.CVE_FONDEADOR
  UNION
  SELECT P.CVE_GPO_EMPRESA,
    P.CVE_EMPRESA,
    P.ID_PRESTAMO_GRUPO ID_PRESTAMO,
    P.CVE_PRESTAMO_GRUPO CVE_PRESTAMO,
    'GRUPO' APLICA_A,
