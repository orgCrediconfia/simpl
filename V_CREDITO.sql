    RS_GRAL_PERSONA PAF,
    RS_GRAL_USUARIO UAA,
    RS_GRAL_PERSONA PAA,
    SIM_CAT_REGIONAL R,
    RS_GRAL_USUARIO UCS,
    RS_GRAL_PERSONA PCS,
    SIM_CAT_FONDEADOR_LINEA L,
    SIM_CAT_FONDEADOR F
