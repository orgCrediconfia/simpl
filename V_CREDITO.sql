    P.ID_CLIENTE CVE_NOMBRE,
    N.NOM_COMPLETO NOMBRE,
    P.ID_PRODUCTO,
    O.NOM_PRODUCTO,
    P.NUM_CICLO,
    P.ID_PERIODICIDAD_PRODUCTO,
    PE.NOM_PERIODICIDAD PERIODICIDAD_PRODUCTO,
    P.Plazo,
    P.DIA_SEMANA_PAGO,
    P.Cve_Metodo,
    ME.NOM_METODO,
    DECODE(P.ID_PERIODICIDAD_TASA,NULL,
    (SELECT ID_PERIODICIDAD
    FROM SIM_CAT_TASA_REFERENCIA
    WHERE ID_TASA_REFERENCIA =
      (SELECT ID_TASA_REFERENCIA FROM SIM_PRESTAMO_GRUPO
      )
    ),P.ID_PERIODICIDAD_TASA) ID_PERIODICIDAD_TASA,
    PET.NOM_PERIODICIDAD PERIODICIDAD_TASA,
    DECODE(P.VALOR_TASA,NULL,
    (SELECT VALOR
    FROM SIM_CAT_TASA_REFER_DETALLE
    WHERE ID_TASA_REFERENCIA =
      (SELECT ID_TASA_REFERENCIA FROM SIM_PRESTAMO_GRUPO
      )
    AND FECHA_PUBLICACION =
      (SELECT MAX(FECHA_PUBLICACION) FECHA_TASA_REFERENCIA
