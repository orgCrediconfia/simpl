      P.CVE_EMPRESA,
      P.ID_PRESTAMO_GRUPO,
      MAX(T.FECHA_AMORTIZACION) FECHA_FIN
    FROM SIM_PRESTAMO_GRUPO P,
      SIM_PRESTAMO_GPO_DET GD,
      SIM_TABLA_AMORTIZACION T
    WHERE GD.CVE_GPO_EMPRESA = P.CVE_GPO_EMPRESA
    AND GD.CVE_EMPRESA       = P.CVE_EMPRESA
    AND GD.ID_PRESTAMO_GRUPO = P.ID_PRESTAMO_GRUPO
    AND T.CVE_GPO_EMPRESA    = GD.CVE_GPO_EMPRESA
    AND T.CVE_EMPRESA        = GD.CVE_EMPRESA
    AND T.ID_PRESTAMO        = GD.ID_PRESTAMO
    GROUP BY P.CVE_GPO_EMPRESA,
      P.CVE_EMPRESA,
      P.ID_PRESTAMO_GRUPO
    )C,
