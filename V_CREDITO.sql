    (SELECT ID_PRESTAMO FROM SIM_PRESTAMO_GPO_DET
    )
  AND N.CVE_GPO_EMPRESA        = P.CVE_GPO_EMPRESA
  AND N.CVE_EMPRESA            = P.CVE_EMPRESA
  AND N.ID_PERSONA             = P.ID_CLIENTE
  AND M.CVE_GPO_EMPRESA        = P.CVE_GPO_EMPRESA
  AND M.CVE_EMPRESA            = P.CVE_EMPRESA
  AND M.ID_PRESTAMO            = P.ID_PRESTAMO
