    RS_GRAL_PERSONA N,
    SIM_CLIENTE_MONTO M,
    SIM_PRESTAMO_CARGO_COMISION C,
    Sim_Producto O,
    SIM_CAT_METODO ME,
    SIM_CAT_SUCURSAL S,
    RS_GRAL_DOMICILIO D,
    SIM_CAT_PERIODICIDAD PE,
    SIM_CAT_PERIODICIDAD PET,
