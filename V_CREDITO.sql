    B.CARGO_INICIAL,
    P.NUM_LINEA,
    L.CVE_FONDEADOR,
    F.NOM_FONDEADOR
  FROM SIM_PRESTAMO_GRUPO P,
    SIM_GRUPO N,
    Sim_Producto O,
    SIM_CAT_METODO ME,
    (SELECT CVE_GPO_EMPRESA,
      CVE_EMPRESA,
      ID_PRESTAMO_GRUPO,
      SUM(MONTO_AUTORIZADO) MONTO_AUTORIZADO
    FROM SIM_PRESTAMO_GPO_DET
    GROUP BY ID_PRESTAMO_GRUPO,
      CVE_GPO_EMPRESA,
      CVE_EMPRESA
    ) A,
    (SELECT P.CVE_GPO_EMPRESA,
      P.CVE_EMPRESA,
      P.ID_PRESTAMO_GRUPO,
      SUM(C.CARGO_INICIAL) CARGO_INICIAL
    FROM SIM_PRESTAMO_GPO_DET P,
      SIM_PRESTAMO_GPO_CARGO C
    WHERE C.CVE_GPO_EMPRESA   = P.CVE_GPO_EMPRESA
    AND C.CVE_EMPRESA         = P.CVE_EMPRESA
    AND C.ID_PRESTAMO_GRUPO   = P.ID_PRESTAMO_GRUPO
    AND C.ID_FORMA_APLICACION = 1
    GROUP BY P.CVE_GPO_EMPRESA,
      P.CVE_EMPRESA,
      P.ID_PRESTAMO_GRUPO
